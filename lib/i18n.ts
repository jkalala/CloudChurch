/**
 * Detect a sensible language from:
 *   – an explicit ISO-3166 country code (e.g. "BR", "FR", "US")
 *   – otherwise the browser locale (navigator.language)
 *   – finally defaults to Portuguese ("pt")
 */
export function getCountryLanguage(countryCode?: string): Language {
  if (countryCode) {
    const code = countryCode.toUpperCase()
    const map: Record<string, Language> = {
      AO: "pt", // Angola
      BR: "pt", // Brazil
      PT: "pt", // Portugal
      US: "en", // United States
      GB: "en", // United Kingdom
      CA: "en", // Canada
      FR: "fr", // France
      BE: "fr", // Belgium
      CH: "fr", // Switzerland
      ES: "es", // Spain
      AR: "es", // Argentina
      MX: "es", // Mexico
      CO: "es", // Colombia
    }
    if (map[code]) return map[code]
  }

  // Fallback to browser locale
  if (typeof window !== "undefined") {
    const locale = navigator.language.toLowerCase()
    if (locale.startsWith("en")) return "en"
    if (locale.startsWith("fr")) return "fr"
    if (locale.startsWith("es")) return "es"
  }

  return "pt" // Default to Portuguese
}

export function useTranslation(language: Language = "pt") {
  const t = (key: string, params?: { [key: string]: string | number }) => {
    const translation = translations[language]?.[key] || translations.en[key] || key

    if (params) {
      return Object.keys(params).reduce((str, param) => {
        return str.replace(new RegExp(`{{${param}}}`, "g"), String(params[param]))
      }, translation)
    }

    return translation
  }

  return { t }
}

export function getSupportedLanguages() {
  return Object.keys(translations)
}

export function getLanguageName(code: string) {
  const names: { [key: string]: string } = {
    en: "English",
    pt: "Português",
    es: "Español",
    fr: "Français",
  }
  return names[code] || code
}
